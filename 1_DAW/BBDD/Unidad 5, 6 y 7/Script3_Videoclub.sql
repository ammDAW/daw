create database videoclub;
use videoclub;


CREATE TABLE Director(
	nombre VARCHAR(25) PRIMARY KEY,
	nacionalidad VARCHAR(25),
	CHECK (nacionalidad IN 
	  ('EEUU','ESPAÑA','ARGENTINA','FRANCIA','ALEMANIA',
	   'REINO UNIDO','ITALIA','AUSTRALIA'))
	);

CREATE TABLE Pelicula(
	titulo VARCHAR(40) PRIMARY KEY,
	nacionalidad VARCHAR(25) DEFAULT 'EEUU' NOT NULL,
	CHECK (nacionalidad IN 
	  ('EEUU','ESPAÑA','ARGENTINA','FRANCIA','ALEMANIA',
	   'REINO UNIDO','ITALIA','AUSTRALIA')),
	productor VARCHAR(25),
	fecha NUMERIC(4) NOT NULL,
	nombre_director VARCHAR(25) NOT NULL REFERENCES Director
	);

CREATE TABLE Copia(
	titulo VARCHAR(25) REFERENCES Pelicula,
	num_copia NUMERIC(2),
	conservacion VARCHAR(20) NOT NULL,
	CHECK (conservacion IN ('BUENO','REGULAR','MALO')),
	PRIMARY KEY (titulo,num_copia)
	);

CREATE TABLE Actor(
	nombre VARCHAR(25) PRIMARY KEY,
	nacionalidad VARCHAR(25) NOT NULL,
	CHECK (nacionalidad IN 
	  ('EEUU','ESPAÑA','ARGENTINA','FRANCIA','ALEMANIA',
	   'REINO UNIDO','ITALIA','AUSTRALIA')),
	sexo CHAR(1) NOT NULL,
	CHECK (sexo IN ('H','M'))
	);

CREATE TABLE Participa(
	titulo VARCHAR(25) REFERENCES Pelicula,
	nombre VARCHAR(25) REFERENCES Actor,
	tipo_papel VARCHAR(15) DEFAULT 'PROTAGONISTA', 
	CHECK (tipo_papel IN ('PROTAGONISTA','SECUNDARIO','REPARTO','FIGURACION')),
	PRIMARY KEY (titulo,nombre)
	);

CREATE TABLE Socio(
	dni CHAR(8) PRIMARY KEY,
	nombre VARCHAR(25) NOT NULL,
	telefono CHAR(9),
	direccion VARCHAR(50),
	avalista CHAR(8) REFERENCES Socio
	);

CREATE TABLE Alquiler(
	titulo VARCHAR(25) REFERENCES Copia,
	num_copia NUMERIC(2) REFERENCES Copia,
	dni_socio CHAR(8) REFERENCES Socio,
	fecha DATE NOT NULL,
	devolucion CHAR(2) DEFAULT 'NO' NOT NULL,
	CHECK (devolucion IN ('NO','SI')),
	PRIMARY KEY (titulo,num_copia,dni_socio,fecha)
	);




